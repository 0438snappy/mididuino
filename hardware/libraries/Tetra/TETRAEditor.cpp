int TETRAEditorClass::mod(int x, int m) {
    int r = x%m;
    return r<0 ? r+m : r;
}

void TETRAEditorClass::setPage(int i){
	index = limit_value(i, 0, 0, NUM_TETRA_EDITOR_PAGES);
	currentPage = &pages[index];
};

void TETRAEditorClass::setPageDown(){
	index = mod(index-1, NUM_TETRA_EDITOR_PAGES);
	currentPage = &pages[index];
};

void TETRAEditorClass::setPageUp(){
	index = mod(index+1, NUM_TETRA_EDITOR_PAGES);
	currentPage = &pages[index];
};

